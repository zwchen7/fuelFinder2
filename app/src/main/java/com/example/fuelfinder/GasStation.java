package com.example.fuelfinder;

public class GasStation {
    private double x;
    private double y;
    private int id;
    private String county;
    private String name;
    private String address;
    private String city;
    private String zip;
    private String phone;
    private double lat;
    private double lng;
    private int gasoline;
    private int diesel;
    private int hv_diesel;
    private int def;
    private int kerosene;
    private int nonHwy;

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public int getGasoline() {
        return gasoline;
    }

    public void setGasoline(int gasoline) {
        this.gasoline = gasoline;
    }

    public int getDiesel() {
        return diesel;
    }

    public void setDiesel(int diesel) {
        this.diesel = diesel;
    }

    public int getHv_diesel() {
        return hv_diesel;
    }

    public void setHv_diesel(int hv_diesel) {
        this.hv_diesel = hv_diesel;
    }

    public int getDef() {
        return def;
    }

    public void setDef(int def) {
        this.def = def;
    }

    public int getKerosene() {
        return kerosene;
    }

    public void setKerosene(int kerosene) {
        this.kerosene = kerosene;
    }

    public int getNonHwy() {
        return nonHwy;
    }

    public void setNonHwy(int nonHwy) {
        this.nonHwy = nonHwy;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCounty() {
        return county;
    }

    public void setCounty(String county) {
        this.county = county;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getZip() {
        return zip;
    }

    public void setZip(String zip) {
        this.zip = zip;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLng() {
        return lng;
    }

    public void setLng(double lng) {
        this.lng = lng;
    }
}
